#include "3-calc.h"
/**
 * op_add - suma
 * @a: numero
 * @b: numero
 * Return: always 0
 **/
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - resta
 * @a: numero
 * @b: numero
 * Return: always 0
 **/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplica
 * @a: numero
 * @b: numero
 * Return: always 0
 **/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divicion
 * @a: numero
 * @b: numero
 * Return: always 0
 **/
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - resto
 * @a: numero
 * @b: numero
 * Return: always 0
 **/
int op_mod(int a, int b)
{
	return (a % b);
}
